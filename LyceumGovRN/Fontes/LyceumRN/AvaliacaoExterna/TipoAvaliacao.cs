        public ValidacaoDados ValidaRemocao(int tipoAvaliacao)
        {
            List<string> mensagens = new List<string>();
            DataContext contexto = null;
            RN.AvaliacaoExterna.Avaliacao rnAvaliacao = new Avaliacao();
            ValidacaoDados validacaoDados = new ValidacaoDados
            {
                Valido = false
            };

            if (tipoAvaliacao <= 0)
            {
                mensagens.Add("Campo ID é obrigatório.");
            }

            if (mensagens.Count == 0)
            {
                try
                {
                    contexto = DataContextBuilder.FromLyceum.ToFastReadingOnly();

                    if (rnAvaliacao.PossuiTipoPor(contexto, tipoAvaliacao))
                    {
                        mensagens.Add("Este Tipo Avaliação não pode ser excluído porque possui avaliações vinculadas. Exclua as avaliações antes.");
                    }                    
                }
                catch
                {
                    if (contexto != null)
                    {
                        contexto.Abandon();
                    }
                    throw;
                }
                finally
                {
                    if (contexto != null)
                    {
                        contexto.Dispose();
                    }
                }
            }

            if (mensagens.Count > 0)
            {
                validacaoDados.Mensagem = mensagens.Aggregate((x, y) => x + Environment.NewLine + y);
            }
            else
            {
                validacaoDados.Valido = true;
            }

            return validacaoDados;
        }

