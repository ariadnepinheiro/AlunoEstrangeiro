        public ValidacaoDados ValidaRemocao(int motivoCancelamentoId)
        {
            List<string> mensagens = new List<string>();
            DataContext contexto = null;
            RN.Ocorrencias.Ocorrencia rnOcorrencia = new Ocorrencia();
            ValidacaoDados validacaoDados = new ValidacaoDados
            {
                Valido = false
            };

            if (motivoCancelamentoId <= 0)
            {
                mensagens.Add("Campo ID é obrigatório.");
            }

            if (mensagens.Count == 0)
            {
                try
                {
                    contexto = DataContextBuilder.FromLyceum.ToFastReadingOnly();

                    //Verifica se ja foi utilizado
                    if (rnOcorrencia.PossuiMotivoCancelamentoPor(contexto, motivoCancelamentoId))
                    {
                        mensagens.Add("Este Motivo de Cancelamento não pode ser excluído, pois já foi utilizado em um Registro de Violência Escolar.");
                    }
                }
                catch
                {
                    if (contexto != null)
                    {
                        contexto.Abandon();
                    }
                    throw;
                }
                finally
                {
                    if (contexto != null)
                    {
                        contexto.Dispose();
                    }
                }
            }

            if (mensagens.Count > 0)
            {
                validacaoDados.Mensagem = mensagens.Aggregate((x, y) => x + Environment.NewLine + y);
            }
            else
            {
                validacaoDados.Valido = true;
            }

            return validacaoDados;
        }

